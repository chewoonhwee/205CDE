                    <h4>About Kap's</h4>
                    <p>Kap's's Pizza started with just one store called "Kappa" bought by brothers Tom and James Monaghan for $500 in 1960. James traded his half of the business to Tom in 1965, and as sole owner Tom renamed the business Kap's's Pizza Inc. In 1978 the 200th Kap's's store opened, and things really began to cook.
                        By 1983 there were 1,000 Kap's's stores, and in the same year Kap's's opened its first international store in Winnipeg, Canada, followed by its first store on the Australasian continent in Queensland, Australia in the same year. By 1989 Kap's's had 5,000 stores in operation, making it the fastest-growing pizza company in the world, with stores in the UK, Japan, and South America.
                        Delivering more than 1 million pizzas a day worldwide, Kap's’s Pizza is the world’s leading and fastest growing pizza delivery company committed to upholding the industry in product quality and operational excellence. Globally, Kap's’s Pizza Inc. operates a total network of more than 14,800 owned and franchised stores throughout the U.S. and in over 85 markets. Kap's’s internationally ranks in the top five companies in online transactions.
                        As at Q2 2017, Kap's’s Pizza Malaysia is the largest Kap's’s market in Southeast Asia and fifth largest in Asia Pacific. Established in 1997, Kap's’s Malaysia is managed by master franchise holder, Dommal Food Services Sdn Bhd. To date, there are more than 240 Kap's’s Pizza stores in the country.
                        At Kap's’s Pizza, we take PRIDE in our commitment to enrich the lives of our customers, employees, partners, shareholders and communities. As one of the most lovable brands in Malaysia, we will go the extra mile for your smile as well as uphold our culture to ‘sell more pizza, have more fun’. Embracing our company’s philosophy of smart hustle, Kap's’s is committed to provide customers with the ultimate pizza delivery experience, with its product satisfaction guarantee, 30 minute delivery guarantee and 15 minute take-away guarantee.
                        Forging ahead as an e-commerce entity, Kap's’s Pizza has led many firsts in its digital platforms. Kap's’s Pizza Malaysia has leveraged on the digital world to the best of its advantage and this began when it was certified as the first QSR company to provide an online ordering platform by the Malaysian Book of Records with the launch of its website in 2003. Kap's's continue to strive and grow digitally and in its innovation to provide better services and customer experience.
                        Kap's’s Malaysia is the award winner of the 2009, 2010, 2011, 2012 and 2016 Gold Franny Award, a distinguished Achievement Award of the International Franchise Association at the Kap's’s Pizza Worldwide Rally. In a show of confidence by Malaysian consumers, Kap's’s Pizza won Bronze in the "Restaurant & Fast Food" category of the prestigious Putra Brand Awards 2014, silver in 2015, bronze in 2016, Silver in 2017 and bronze in 2018.
                        Kap's’s Pizza Malaysia has grown by leaps and bounds since it first began its operations in 1997. From a single store in USJ with a headcount of 15 staffs, the pizza chain today has over 240 stores in Malaysia and over 30 stores in Singapore with a headcount of over 4,000 staffs. Kap's’s Pizza Malaysia has experienced solid growth, particularly in the past few years with the opening of its 50th store in January 2011, 100th store in December 2012, 150th store in March 2016 and now the 200th store in April 2017.</p>
